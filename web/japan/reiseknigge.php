              Den Reis solltest du unter allen Umständen aufessen, da es sich hierbei um ein wichtiges Grundnahrungsmittel handelt.
              Falls dir einmal die Nase laufen sollte, vermeide es unter allen Umständen, sie am Tisch zu säubern, das gilt als sehr unhygienisch.
              Entschuldige dich und geh auf die Toilette.
              Alternativ kannst du auch die Nase hochziehen.
              In manchen Reiseführern wirst du die Angabe finden, dass gewisse Darmwinde in Japan beim Essen durchaus normal seien.
              Das kann man so nicht stehen lassen.
              Ebenso wie in Deutschland gehören Rülpsen und Furzen nicht zur guten Tischsitte.
              Wohl aber ist es bei manchen Speisen (Nudeln) erlaubt, wenn du sie "hochschlotzt", was ja mit einem gewissen Geräuschpegel einhergeht.
