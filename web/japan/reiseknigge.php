              In den allermeisten Fällen hat ein Lächeln eine ähnliche Bedeutung wie in Deutschland, jedoch mit einer zusätzlichen Höflichkeitskomponente.
              Versuch daher am besten häufiger zu lächeln.<br />
              Angeregte Diskussionen mit entsprechend starker Mimik und groß aufgerissenen Augen, wie du sie aus Deutschland vielleicht kennst, könnten von Japanern als Streit ausgelegt werden.<br /><br />
              Und wenn wir schon dabei sind: Augenkontakt ist, anders als in Deutschland, unerwünscht.
              Du schaust den Menschen direkt in die "Spiegel ihrer Seele", was als unhöflich bis feindselig aufgefasst wird.
              Schau lieber auf das Gesicht deines Gesprächspartners, am besten auf einen Punkt auf der Stirn. 
