              Generell hängt man in Japan an Vor- oder Nachnamen gerne ein "San" hinten dran.
              Bei jüngeren Gesprächspartnern (und besonders bei Frauen) ein "chan".
              Das ist eine Verniedlichungsform und ist dem deutschen "chen" gar nicht so unähnlich in Schreibweise und Bedeutung.
              Unter alten Freunden ist der Suffix "kun" gebräuchlich, ältere Schüler benutzen gerne ein "senpai".
              Wer als besonders gebildet gilt, wird als "sensei" bezeichnet. "Sama" wird oft in gehobeneren Kreisen angewendet
              Welche Begrüßungsfloskel du verwendest hängt, genau wie in Deutschland, davon ab, ob du mit Freunden / Fremden und zu welcher Tageszeit du unterwegs bist.
