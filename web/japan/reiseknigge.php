          <h2 id="Kommunikation"><span>Kommunikation</span></h2>
          <div class="row">
            <div class="merkzettel">
              <ul class="list-group">
                <li class="list-group-item do list-group-item-success">"Ja" als Aufmerksamkeitsbestätigung verwenden</li>
                <li class="list-group-item do list-group-item-success" >Auf kritik ausweichend reagieren</li>
                <li class="list-group-item do list-group-item-success" >Ablehnung ("Nein") umschreiben</li>
                <li class="list-group-item do list-group-item-success" >Mit smalltalk einsteigen</li>
                <li class="list-group-item dont list-group-item-danger">Mit der Tür ins Haus fallen</li>
                <li class="list-group-item dont list-group-item-danger">Direkt "Nein" sagen</li>
              </ul>
            </div>
            <p>
              Die Unterhaltung in Japan ist selten direkt.
              Besonders dann, wenn ein Konflikt im Raum schwebt.
              Eher wird man versuchen, gegenteilige Meinungen und Kritik sehr ausweichend zu formulieren.
              Du solltest also nie mit der Tür ins Haus fallen, sondern immer erst den Umweg des Small-Talks gehen.
              Ein direktes Ansprechen von Problemen wird als unhöflich erachtet.<br /><br />
              Zusätzlich ist der Gebrauch der Wörter "ja" ("hai", link zu sprachführer) und "nein" ("iie", link zu sprachführer).
              So symbolisiert man mit dem Wort "hai" nicht unbedingt eine Zustimmung, sondern eher eine Art Aufmerksamkeitsbestätigung.
              Besonders bei Telefonaten solltest du immer wieder ein "hai" einstreuen, damit dein Gegenüber weiß, dass du im aufmerksam zuhörst.
              Übertreiben solltest du es damit aber nicht, da ein "jaja" ("haihai") als abwertendes Wort gesehen wird.<br /><br />
              Das Wort "Nein" gibt es zwar, jedoch wirst du es recht selten hören.
              Stattdessen verwenden Japaner eher ausweichende Wendungen wie: "Es ist anders..." ("chigau") oder "das ist ein bisschen..." ("kore wa chotto...").
