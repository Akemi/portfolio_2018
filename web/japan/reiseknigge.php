            <p>
             Im Restaurant wird immer ein feuchtes, sehr warmes Tuch gereicht, dass du vor dem Essen zur Handreinigung nehmen solltest und während des Essens als Serviette benutzen kannst.<br />
              Das gemeinsame Essen wird mit einem gnädigen Dank an den Gastgeber eingeleitet, der diese Speise bereitstellt ("Itadakimasu").
              Wie du sicherlich weißt, wird in Japan mit Stäbchen gegessen.
              Dabei solltest du nie die Stäbchen im Reis stecken lassen.
              Das ist in Japan ein Beerdigungsbrauch!
              Die Stäbchen solltest du nie überkreuzt oder auf gemeinsamen Essplatten ablegen.
              Leg sie lieber nebeneinander neben deinen Teller.
              Du isst immer im Kreis, sodass alle Komponenten in etwa zeitgleich aufgegessen sind.<br />
              Das folgende Video zeigt dir, wie das geht:
