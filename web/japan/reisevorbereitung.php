            <h4 id="frage3,2" class="panel-heading">4.3 Supermärkte<i class="indicator glyphicon glyphicon-chevron-down  pull-right"></i></h4>
            <div class="panel-body">
              Japanische Supermärkte ähneln ihren europäischen Pendants.
              Es finden sich viele westliche Produkte im Sortiment, jedoch sind diese auf Grund des Imports meist teurer als heimische Artikel.
              In kleineren Städten und Vororten findest du diese Einkaufsmöglichkeit sehr oft, in größeren Städten aber kaum noch.
              Interessant zum Geld sparen ist die Tatsache, dass die meisten Supermärkte ihre unverkauften Frühstücksitems ab 14:00 mit moderaten Rabatten von 10-20% anbieten.
              Bis zum Ladenschluss steigern sich die Rabatte auf 50 bis 70%.
            </div>
            <h4 id="frage3,3" class="panel-heading">4.4 Fast Food-Ketten<i class="indicator glyphicon glyphicon-chevron-down  pull-right"></i></h4>
            <div class="panel-body">
              Neben den üblichen verdächtigen wie McDonald's und Kentucky Fried Chicken befinden sich auch einige japanische Fast Food-Ketten auf dem Markt.
              Die Kosten für eine Mahlzeit für eine Person sprengen dabei selten die 10€-Grenze.
              <div class="row">
                <div class="col-xs-offset-3 col-xs-6 well">
                  <img class="img-responsive center-block" src="img/jpnfastfood.jpg"><br />
                  <span class="pull-right caption">Japanische Fast Food-Kette</span>
                </div>
              </div>
