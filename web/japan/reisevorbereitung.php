            <h4 id="frage2,8" class="panel-heading">3.9 Wie orientiere ich mich, wenn ich eine Adresse suche?<i class="indicator glyphicon glyphicon-chevron-down  pull-right"></i></h4>
            <div class="panel-body">
              Die meisten Straßennamen sind neben der japanischen Ausführung auch noch mal in lateinischer Schrift abgebildet.
              Aber eben nicht alle!
              <div class="row">
                <div class="col-xs-offset-3 col-xs-6 well">
                  <img class="img-responsive center-block" src="img/Japanisches Strassenschild.jpg"><br />
                  <span class="pull-right caption">Japanisches Straßenschild</span>
                </div>
              </div>
              Wenn du denkst, dass dein Hostel in der Nakamuracho minami 25 einfach das 25. Haus der Straße ist, irrst du dich gewaltig.
              In Japan gilt bei der Vergabe der Hausnummern das Credo: "Wer zuerst kommt, mahlt zuerst!"
              Ein altes traditionelles Haus erhält die Nummer 1, weil es das erste Gebäude der Straße war.
              Daneben ist jedoch ein Grundstück frei geworden. Das 120. Haus der Straße wird darauf gebaut und erhält die Hausnummer 120.
              Dieses Vergabesystem führt sogar so weit, dass selbst Taxifahrer in großen Ballungsgebieten ratlos sind und sich Hilfe bei einer örtlichen Polizeistation suchen.
              Die verfügen nämlich über eine genau kartografierte Karte ihres Bezirks.
