            <br/> Außerdem solltest du Reiseagenturen, Zeitungsannouncen, Reise-Webseiten nach Angeboten (siehe zum Beispiel <a href="http://www.urlaubspiraten.de/">Urlaubspiraten</a>) durchforsten.
            Zusätzlich besteht die Möglichkeit über die Seite der Fluglinienbetreiber selbst auf Sonderangebote zu stoßen.
            Die meisten großen Airlines besitzen eine Sonderangebote-Sektion auf ihrer Website.<br/>
            Falls deine Heimatstadt eine ausreichend große japanische Population aufweist, ist es wahrscheinlich, dass große japanische Reiseagenturen wie HIS hier eine Filiale betreiben.
