         <h3 id="category4">5. Unterkünfte</h3>
         <div class="panel panel-default accordion">
            <h4 id="frage4,0" class="panel-heading">5.1 Was muss ich bei den Unterkünften beachten?<i class="indicator glyphicon glyphicon-chevron-down  pull-right"></i></h4>
            <div class="panel-body">
              Es ist empfehlenswert, deine Übernachtungsmöglichkeiten im Voraus zu planen.
              Last-Minute-Buchungen, wenn überhaupt möglich, sind keine besonders sinnvolle Taktik in Japan, um kosteneffizient zu nächtigen.
              Wenn du damit leben kannst, auch außerhalb der Städte zu übernachten und längere Fußwege zur nächsten Zug- oder Busstation in Kauf zu nehmen, kannst du ebenfalls Geld sparen.
              Die meisten Vergleichsplattformen sind in Japanisch, 2 gute englischsprachige Seiten sind unter <a href="http://travel.rakuten.com/?scid=af_pc_etc&amp;sc2id=200164375">Rakuten Travel</a> und <a href="http://www.hostelworld.com/">Hostelworld</a> zu finden.
            </div>
            <h4 id="frage4,1" class="panel-heading">5.2 Hostels (15-30€)<i class="indicator glyphicon glyphicon-chevron-down  pull-right"></i></h4>
            <div class="panel-body">
              Die meisten Hostels findest du in größeren Städten und sind eher westlich eingerichtet.
              <div class="row">
                <div class="col-xs-offset-3 col-xs-6 well">
                  <img class="img-responsive center-block" src="img/Japanisches Hostel.jpg"><br />
                  <span class="pull-right caption">Japanisches Hostel</span>
                </div>
              </div>
              Üblich sind dabei Gemeinschaftsräume, die nach Geschlecht getrennt werden.
              Sanitäre Einrichtungen werden dabei ebenfalls zusammen genutzt.
              Du solltest beachten, dass die meisten Hostels in älteren, nicht mehr ganz so modernen Gebäuden zu finden sind. Dafür verleihen die Betreiber oft kostenlos Fahrräder, die du für einen Kurztrip nutzen kannst.
            </div>
            <h4 id="frage4,2" class="panel-heading">5.3 Ryokan, Minshuku (30-100€)<i class="indicator glyphicon glyphicon-chevron-down  pull-right"></i></h4>
            <div class="panel-body">
              Etwas mehr Komfort als auch japanischen Stil besitzen die sogenannten Minshuku.
              Das sind japanisch-traditionell eingerichtete Hotels, bei denen Frühstück und baden in einer heißen Quelle inklusive sind.
              Ryokan sind den Minshuku sehr ähnlich, wenngleich auch etwas luxuriöser.
              Meist wird ein Restaurant betrieben, welches lokale Speisen anbietet.
              Wer Japan erleben will, sollte unbedingt (zumindest für eine Nacht) eine dieser beiden Unterkunftsarten in Betracht ziehen.<br />
              <div class="row">
                <div class="col-xs-offset-1 col-xs-4 well">
                  <img class="img-responsive center-block" src="img/Minshuku.jpg"><br />
                  <span class="pull-right caption">Minshuku</span>
                </div>
                <div class="col-xs-offset-1 col-xs-4 well">
                  <img class="img-responsive center-block" src="img/Ryokan.jpg"><br />
                  <span class="pull-right caption">Ryokan</span>
                </div>
              </div>
            </div>
            <h4 id="frage4,3" class="panel-heading">5.4 Geschäftshotels (50-70€)<i class="indicator glyphicon glyphicon-chevron-down  pull-right"></i></h4>
            <div class="panel-body">
              Geschäftshotels bieten kleine, recht einfache westlich-eingerichtete Zimmer an, die meistens sehr nah an Zughaltestellen liegen.
              <div class="row">
                <div class="col-xs-offset-3 col-xs-6 well">
                  <img class="img-responsive center-block" src="img/Zimmer eines Geschaftshotels.jpg"><br />
                  <span class="pull-right caption">Zimmer eines Geschäftshotels</span>
                </div>
              </div>
            </div>
            <h4 id="frage4,4" class="panel-heading">5.5 Internetcafes (10-30€)<i class="indicator glyphicon glyphicon-chevron-down  pull-right"></i></h4>
            <div class="panel-body">
              Was in Deutschland unvorstellbar wäre, ist in Japan eine sehr günstige Alternative zu nächtigen.
              Auf Kosten der Privatssphäre kannst du hier zwischen den Tischen schlafen.
              Etwas mehr Ruhe bieten dann private Räume, die meistens mit Matten oder Couches zum Schlafen ausgestattet sind.
            </div>
            <h4 id="frage4,5" class="panel-heading">5.6 Kapselhotels (25-50€)<i class="indicator glyphicon glyphicon-chevron-down  pull-right"></i></h4>
            <div class="panel-body">
              Hierbei schläft man in mehr oder minder großen Kapseln, welche neben einem Fernseher, Internet, Licht und Heizung nicht sonderlich viel Luxus zu bieten haben.
              Santiäre Einrichtungen teilst du dir dabei mit den anderen Hotelbesuchern.
              Persönliche Dinge verschließst du dabei in einem Schließfach. In Betracht gezogen werden sollten diese Hotels aufgrund ihrer guten Lage an größeren Zugstationen.
              <div class="row">
                <div class="col-xs-offset-3 col-xs-6 well">
                  <img class="img-responsive center-block" src="img/Kapselhotel.jpg"><br />
                  <span class="pull-right caption">Kapselhotel</span>
                </div>
              </div>
            </div>
