            <div class="panel-body">
            Wie der Name es bereits vermuten lässt, handelt es sich hier um eine öffentliche Badeeinrichtung, die 24h am Tag geöffnet hat.
            Finden kannst du diese Bäder in jeder größeren Stadt, die meisten von ihnen bieten den Besuchern auch Schlafmöglichkeiten an.
